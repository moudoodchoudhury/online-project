package ArrayPractice;

public class MultipleAlphabet {
	public static void main(String[] args) {
		
		System.out.println("Starts A-z:");
		
		for (char x ='a';x<='z';x++) {
			System.out.println(x);
		}
		System.out.println("ends A-z:");
		
	System.out.println("Starts Z-A:");
		
		for (char x ='z';x>='a';x--) {
			System.out.println(x);
		}
		System.out.println("ends Z-A:");
	}

}
